a = 5
b = 10

if a == NaN:
    print("Error: Value of number A is invalid.")

if b == NaN:
    print("Error: Value of number B is invalid.")

if a == b:
    print("Number A equals Number B")

if a < b:
    print("Number A is less than Number B")
    
if a > b:
    print("Number A is greater than Number B")
